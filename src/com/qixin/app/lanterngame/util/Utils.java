package com.qixin.app.lanterngame.util;

import org.apache.poi.util.SystemOutLogger;

public class Utils {
	public static String addZeroForJobNumbe(String jobNumber) {
		int strLength = 10;
		int strLen = jobNumber.length();
		StringBuffer sb = new StringBuffer();
		if (strLen < strLength) {	
		while (strLen < strLength) {
			sb.append("0");//左补0
			strLen++;
			}
		
		}
		sb.append(jobNumber);
		return sb.toString();
		}
	
}
